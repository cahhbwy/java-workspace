package chap3;

public class Demo3 {

	public static void main(String[] args) {
		int n=2;
		switch(n){
		case 0:
			System.out.println("case 0执行");
		case 1:
			System.out.println("case 1执行");
		case 2:
			System.out.println("case 2执行");
		case 3:
			System.out.println("case 3执行");
		case 4:
			System.out.println("case 4执行");
		case 5:
			System.out.println("case 5执行");
		case 6:
			System.out.println("case 6执行");
			break;
		default:
			System.out.println("default执行");
		}
	}

}
